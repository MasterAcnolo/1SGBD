-- Les Canadiens ont annexé les états américains du Michigan, du Wisconsin, de l'Illinois, de l'Alaska et de Washington.

-- Les anciennes provinces du Québec et de l'Ontario forment désormais un état souverain, dont la capitale est Montréal.

-- Les États-Uniens ont conquis de nouvelles terres au sud : ils occupent désormais le Mexique, Cuba, le Venezuela et la Colombie.

-- En réaction, tous les pays hispanophones au sud de la Colombie et du Brésil forment désormais un unique pays : les Nouveaux États-Unis d'Amérique.