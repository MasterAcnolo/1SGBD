-- En France, la Bretagne a colonisé la Normandie et forme désormais un nouvel état souverain, dont la langue officielle est le breton et dont la capitale est Rennes.

-- La région Centre a déclaré son indépendance, s’est baptisée « République du Val de Loire » et a fait de Tours sa capitale.

-- Lyon, Caen et Lille sont désormais trois cités-États indépendantes.

-- Le Liechtenstein a annexé la Suisse et la Lombardie. 